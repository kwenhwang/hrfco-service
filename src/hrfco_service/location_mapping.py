def get_location_coordinates(address: str) -> Optional[Tuple[float, float]]:
    """주소를 좌표로 변환합니다.
    
    Args:
        address: 주소 문자열
        
    Returns:
        (위도, 경도) 튜플 또는 None
    """
    # 정확한 매칭
    if address in LOCATION_MAPPING:
        return LOCATION_MAPPING[address]
    
    # 부분 매칭 시도
    for region, coords in LOCATION_MAPPING.items():
        if region in address or address in region:
            return coords
    
    return None

